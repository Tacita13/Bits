/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
  int firstHex=x>>(1<<2);
  int secondHex= x & 0xF;
  // if first digit is 3 and second-10 is negative, then it is an Ascii digit
    return (!(firstHex^3) & !!((secondHex+(~10+1)) >> 31));
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
    
    int xIsNeg=(x >> 31);
    int yIsNeg=(y >> 31);
    int xorNeg= xIsNeg^yIsNeg;
    return  !(((((~x+1)+y) >>31) | xorNeg) ^ (xorNeg & xIsNeg));
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
 // arithmetical shift and mask that converts it to logical shift
 return (x>>n) & ~(~0 << (32 + ~n) << 1);
}
